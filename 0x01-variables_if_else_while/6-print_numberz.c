#include <ctype.h>
#include <stdlib.h>
#include <stdio.h>
/**
 *main - printing base 10 numbers starting from 0
 *Description: using the main fuction
 *the program prints "prints base 10 numbers"
 *Return: 0
 */
int main(void)
{
int mi;
for (mi = 0 ; mi < 10 ; mi++)
{
        putchar(mi);
}
putchar("\n");
return (0);
}
